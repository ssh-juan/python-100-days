# 'for' Loop for Lists
# Aqui está uma explicação de como funciona:
# 1. Loop for: é usado para iterar sobre cada item na lista fruits.
# 2. Variável 'fruit': Em cada iteração do loop, a variável fruit assume o valor do próximo item na lista.
# Na primeira será "Apple", na segunda será "Peach" e na terceira será "Pear".

fruits = ["Apple", "Peach", "Pear"]

for fruit in fruits:
    print(fruit)
    print(fruit + " pie")
    print(fruits)

print(fruits)